# Задача 1: Времена года
# Попросить пользователя ввести с клавиатуры номер месяца (проверка корректности ввода не требуется).
# Определить и вывести на экран время года: winter, spring, summer, autumn.

month = int(input('Введите номер месяца: '))
dct_month = {
    'winter': [12, 1, 2],
    'spring': [3, 4, 5],
    'summer': [6, 7, 8],
    'autumn': [9, 10, 11]
}

output = None
for season, months in dct_month.items():
    # Проверяем, входит ли номер месяца в список
    if month in months:
        output = season
        print(f'Ваш месяц: {output}')
        break

if output is None:
    print('Введите корректный номер месяца')

# Задача 2: Цвет светофора
# Программа определяет, какого цвета сигнал горит в указанный момент времени.
# Зеленый сигнал горит первые три минуты каждого 5-минутного цикла, красный — следующие две минуты.

minute = int(input("Введите количество минут от начала часа: "))

# Уменьшаем минуты до диапазона одного цикла (0–4)
while minute >= 5:
    minute -= 5

if minute <= 2:  # Если это 0-я, 1-я или 2-я минута
    print("green")
else:  # Если это 3-я или 4-я минута
    print("red")

# Задача 3: Палиндром
# Проверить, является ли введенная строка или число палиндромом.

import re

polindrom = input("Введите строку или число: ")

# Удаление всех символов, кроме букв и цифр, и приведение к нижнему регистру
cleaned = re.sub(r'[^a-zA-Z0-9]', '', polindrom).lower()

# Проверка на палиндром
if cleaned == cleaned[::-1]:
    print('Да, это палиндром')
else:
    print('Нет, это не палиндром')
